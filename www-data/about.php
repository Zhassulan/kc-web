<h3>О Клиринговом центре <small>ЕТС</small></h3>

<p>
Товарищество с ограниченной ответственностью «Клиринговый центр ЕТС» (далее – Клиринговый Центр) зарегистрировано в органах юстиции Республики Казахстан 03 августа 2009 года и осуществляет специализированную клиринговую деятельность на протяжении 7 лет.
</p>

<ins><p>Учредителями (участниками) Клирингового центра являются:</p></ins>

<div id="about_fragment">
		<ol>
			<li>Акционерное общество «Товарная Биржа «Евразийская торговая система» (Республика Казахстан, г. Алматы), доля которого в Уставном капитале Клирингового центра составляет 74,9%;</li>
		  	<li>Публичное акционерное общество «Московская Биржа ММВБ-РТС» (Российская Федерация, г. Москва» (далее – Московская Биржа»), доля которого в Уставном капитале Клирингового центра составляет 25,1%.</li>
		</ol>
</div>
