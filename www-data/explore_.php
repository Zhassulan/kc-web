<h3>Технологии расчета и клиринга, применяемые на товарных рынках</h3>
<p>&nbsp;</p>
<p><strong>ТОО &laquo;Клиринговый центр ЕТС&raquo; </strong>(далее &ndash; Клиринговый центр) было зарегистрировано в Департаменте юстиции г. Алматы в августе 2009 года.</p>
<p><span style="text-decoration: underline;">Основной задачей Клирингового Центра является</span>:</p>
<ul>
    <li>Оказание клиринговых услуг участникам торгов на <strong>АО &laquo;Товарная биржа &laquo;ЕТС&raquo; и </strong>других товарных биржах Республики Казахстан;</li>
    <li>Развитие расчетно-клиринговых систем в соответствии с мировыми стандартами;</li>
    <li>Эффективное разграничение рисков в процессе функционирования КЦ.</li>
</ul>
<p><span style="text-decoration: underline;">Клиринговый Центр осуществляет свою деятельность на основе</span>:</p>
<ul>
    <li>Закона РК &laquo;О товарных биржах&raquo;</li>
    <li>Правил биржевой торговли и правил клиринга</li>
    <li>Договора с Биржей о порядке взаимодействия по осуществлению клиринга при проведении торгов и осуществлению расчетов по сделкам, заключенным на торгах</li>
</ul>
<p>&nbsp;<span style="text-decoration: underline;">Клиринговый центр - как агент по сделкам, заключенными на Бирже:</span></p>
<ul>
    <li>&nbsp;Выступает гарантом исполнения обязательств сторон и берет на себя риски участников, по заключаемым в ходе биржевых торгов сделкам,</li>
    <li>&nbsp;выступает посредником между сторонами, т.е. является&nbsp; продавцом для каждого покупателя и покупателем для каждого продавца,</li>
    <li>заменяет собой договорные отношения продавца и покупателя между собой, соответствующими договорными обязательствами с агентом.</li>
</ul>

