<p><span style="text-decoration: underline;">Система биржевого обеспечения выполняет следующие функции:</span></p>
<ol>
    <li>обеспечивает защиту участников сделки от риска, связанного с неисполнением одним из участников своих обязательств по сделке,</li>
    <li>обеспечивает полное покрытие и без акцептное перечисление суммы неустойки, причитающиеся пострадавшей стороне, в случае нарушения условий биржевой сделки.</li>
</ol>

