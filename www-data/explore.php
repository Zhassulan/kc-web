<h3>Технологии расчета и клиринга, применяемые на товарных рынках</h3>
<p>&nbsp;</p>

<p><span><strong>ТОО &laquo;Клиринговый центр ЕТС&raquo;</strong> (далее &ndash; Клиринговый центр) было зарегистрировано в Департаменте юстиции г. Алматы в августе 2009 года.</span></p>
<p><span><span style="text-decoration: underline;">Основной задачей Клирингового Центра является</span>:</span></p>
<ol>
    <li>Оказание клиринговых услуг участникам торгов на АО &laquo;Товарная биржа &laquo;ЕТС&raquo; и других товарных биржах Республики Казахстан;</li>
    <li>Развитие расчетно-клиринговых систем в соответствии с мировыми стандартами;</li>
    <li>Эффективное разграничение рисков в процессе функционирования КЦ.</li>
</ol>
<p><span style="font-size: small;"><span style="text-decoration: underline;">Клиринговый Центр осуществляет свою деятельность на основе</span>:</span></p>
<ol>
    <li><span>Закона РК &laquo;О товарных биржах&raquo;</span></li>
    <li><span >Правил осуществления клиринговой деятельности по биржевым сделкам</span></li>
    <li><span>Правил клиринга</span></li>
    <li><span>Договора с Биржей о клиринговом обслуживании</span></li>
</ol>

